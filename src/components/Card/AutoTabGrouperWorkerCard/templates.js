function formatDomainName(val) {
  // Strip "www." and grab the first subdomain/name segment
  const cleaned = val.replace(/^www\./, '');
  const firstPart = cleaned.split('.')[0];

  if (DOMAIN_DISPLAY_OVERRIDES[firstPart]) {
    return DOMAIN_DISPLAY_OVERRIDES[firstPart];
  }

  // Capitalize first letter
  return firstPart.charAt(0).toUpperCase() + firstPart.slice(1);
}

