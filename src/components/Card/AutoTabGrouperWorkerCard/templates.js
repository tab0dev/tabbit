// Flat lookup helper: any id → { label, title }
const _regionFlat = (() => {
  const m = {};
  function walk(nodes) {
    for (const n of nodes) {
      m[n.id] = { label: n.label, title: n.title };
      if (n.children?.length) walk(n.children);
    }
  }
  walk(REGION_TREE);
  return m;
})();

export function getRegionMeta(regionId) {
  return _regionFlat[regionId] ?? { label: '🌐', title: regionId };
}

